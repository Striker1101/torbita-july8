//for

/**
 for(initialization; condition; counter{increment or decrement}){
   //statment
 } 
 */

// i = i+1
for (let i = 0; i >= 10; i++) {
  //statement
  console.log(i);
}
console.log("............................ for loop");

/**
 while (condition) {
    
   counter{ increment or decrement}
}
 */
let i = 0;
while (i >= 10) {
  //statement
  console.log(i);

  //counter
  i++;
}

console.log("............................ while loop");

/**
 do {

   counter{increment or decrement}
} while (condition);
 */

let j = 0;
do {
  //statement
  console.log(j);

  //counter
  j++;
} while (j > 10);

console.log("............................do while loop");

